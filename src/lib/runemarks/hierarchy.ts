// ── Lookup helpers ────────────────────────────────────────────────────────────

export function getAllianceSvg(alliance: string): string | null {
	return hierarchy.find(a => a.label === alliance)?.svg ?? null;
}

export function getFactionSvg(alliance: string, faction: string): string | null {
	return hierarchy.find(a => a.label === alliance)
		?.factions.find(f => f.label === faction)?.svg ?? null;
}

export function getSubfactionSvg(alliance: string, faction: string, subfaction: string): string | null {
	return hierarchy.find(a => a.label === alliance)
		?.factions.find(f => f.label === faction)
		?.subfactions.find(s => s.label === subfaction)?.svg ?? null;
}

export function getFactions(alliance: string): FactionEntry[] {
	return hierarchy.find(a => a.label === alliance)?.factions ?? [];
}

export function getSubfactions(alliance: string, faction: string): SubfactionEntry[] {
	return hierarchy.find(a => a.label === alliance)
		?.factions.find(f => f.label === faction)?.subfactions ?? [];
}
