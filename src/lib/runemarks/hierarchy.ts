// ── Lookup helpers (keyed on id) ──────────────────────────────────────────────

export function getAllianceSvg(allianceId: string): string | null {
	return hierarchy.find(a => a.id === allianceId)?.svg ?? null;
}

export function getFactionSvg(allianceId: string, factionId: string): string | null {
	return hierarchy.find(a => a.id === allianceId)
		?.factions.find(f => f.id === factionId)?.svg ?? null;
}

export function getSubfactionSvg(allianceId: string, factionId: string, subfactionId: string): string | null {
	return hierarchy.find(a => a.id === allianceId)
		?.factions.find(f => f.id === factionId)
		?.subfactions.find(s => s.id === subfactionId)?.svg ?? null;
}

export function getFactions(allianceId: string): FactionEntry[] {
	return hierarchy.find(a => a.id === allianceId)?.factions ?? [];
}

export function getSubfactions(allianceId: string, factionId: string): SubfactionEntry[] {
	return hierarchy.find(a => a.id === allianceId)
		?.factions.find(f => f.id === factionId)?.subfactions ?? [];
}
